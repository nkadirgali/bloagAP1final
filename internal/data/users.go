func (m UserModel) GetAllUsers() ([]*User, error) {
	query := `
	SELECT count(*) OVER(),id, created_at, username, firstname, lastname, email, password_hash, version
	FROM users`
	ctx, cancel := context.WithTimeout(context.Background(), 3*time.Second)
	defer cancel()
	rows, err := m.DB.QueryContext(ctx, query)
	// err := m.DB.QueryRowContext(ctx, query).Scan(
	// 	&user.ID,
	// 	&user.CreatedAt,
	// 	&user.Username,
	// 	&user.Firstname,
	// 	&user.Lastname,
	// 	&user.Email,
	// 	&user.Password.hash,
	// 	&user.Version,
	// )
	if err != nil {
		return nil, err // Update this to return an empty Metadata struct.
	}

	defer rows.Close()
	// Declare a totalRecords variable.
	totalRecords := 0

	users := []*User{}

	for rows.Next() {
		var user User
		err := rows.Scan(
			&totalRecords, // Scan the count from the window function into totalRecords.
			&user.ID,
			&user.CreatedAt,
			&user.Username,
			&user.Firstname,
			&user.Lastname,
			&user.Email,
			&user.Password.hash,
			&user.Version,
		)
		if err != nil {
			return nil, err
		}
		users = append(users, &user)
	}

	if err = rows.Err(); err != nil {
		return nil, err
	}
	return users, nil
}
